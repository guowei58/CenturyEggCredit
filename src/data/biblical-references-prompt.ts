export function buildBiblicalReferencesSystemPrompt(ticker: string, companyName?: string): string {
  const co = companyName?.trim() ? companyName.trim() : "(not specified)";
  return BIBLICAL_REFERENCES_BODY.replace("[INSERT_TICKER]", ticker.trim().toUpperCase()).replace(
    "[INSERT_COMPANY]",
    co
  );
}

export function buildBiblicalReferencesUserPrompt(params: { inventory: string; materials: string }): string {
  return `
# FILE / MATERIAL INVENTORY
${params.inventory}

# RESEARCH MATERIALS (primary basis — read carefully)
${params.materials}

---
Follow the OUTPUT FORMAT in your system instructions. Write the full response in Markdown.
`.trim();
}
