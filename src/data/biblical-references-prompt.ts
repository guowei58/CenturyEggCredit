- "This is less Job and more Ecclesiastes with leverage."
- "Management talks like Joshua entering Canaan, but the numbers look more like Israel asking for a king."
- "This is not resurrection; it is Lazarus still wrapped in bandages."
- "The deck is full of Promised Land language, but the covenantal fruit is not there."
- "The company is treating liquidity like manna: enough for today, no plan for tomorrow."

